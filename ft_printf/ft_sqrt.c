#include <libft.h>

double	ft_sqrt(double nb)
{
	int		i;
	double	x;

	i = 0;
	x = nb;
	while (x > 1)
	{
		x = x / 10;
		i++;
	}
	x = i;
	while (i > 1)
	{
		x = x * 10;
		i--;
	}
	i = x;
	while (i > -10)
	{
		x = (double)1 / 2 * (x + (nb / x));
		i--;
	}
	return (x);
}
